CREATE TABLE USUARIOS (
    ID_USUARIO NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) UNIQUE NOT NULL,
    TIPO_TRABALHO VARCHAR2(20) CHECK (TIPO_TRABALHO IN ('Home Office', 'Presencial', 'Híbrido'))
);

CREATE TABLE TAREFAS (
    ID_TAREFA NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    ID_USUARIO NUMBER NOT NULL,
    TITULO VARCHAR2(100) NOT NULL,
    DESCRICAO VARCHAR2(255),
    TIPO VARCHAR2(20) CHECK (TIPO IN ('Pessoal', 'Profissional')),
    DATA_CRIACAO DATE DEFAULT SYSDATE,
    DATA_CONCLUSAO DATE,
    STATUS VARCHAR2(20) DEFAULT 'Pendente' CHECK (STATUS IN ('Pendente', 'Concluída')),
    FOREIGN KEY (ID_USUARIO) REFERENCES USUARIOS(ID_USUARIO)
);
